# Arithmetic Operators:
# Shortcut to run program: ctrl,shift, F10

# addition
print(5 + 5)

# subtraction
print(3 - 9)

# division
print( 8/ 4)

# //   integer division, if has remainder, won't be displayed
print(11// 2)           # 5

# multiplication
print( 5 * 4)

# ** Raise to power
print(2 ** 100)         # 1267650600228229401496703205376

# Modulus aka Mod: remainder
print(8 % 5)            # 3
print(14 % 4)           # 2

print(4 * 5.0)          # 20.0  int * float = float












