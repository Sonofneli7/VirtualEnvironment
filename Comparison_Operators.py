# Comparison Ops: compare 2 values
# Return T or F according to condition


# ==

print(7 == 8)           # => False
print('abc' == 'abc')   # => True

# != (not equal)
print(7 != 7)           # => False
print('a' != 'A')       # => True

# > (greater than) L > R
a, b = 7,9
print (a > b)           # => False

# >= (greater than, equal to) L >= R
print(b >= 9)           # => True

# < (less than) L < R
print (a < 8)           # => True

# <= (less than, equal to) L <= R
print( a * 2 <= 100)    # 7*2 <= 100 => True